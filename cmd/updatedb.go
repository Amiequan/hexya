package cmd

import (
	"path/filepath"
	"text/template"

	"github.com/hexya-erp/hexya/src/models"
	"github.com/hexya-erp/hexya/src/server"
	"github.com/spf13/cobra"
	"github.com/spf13/viper"
)

const updateDBFileName string = "updatedb.go"

var updateDBCmd = &cobra.Command{
	Use:   "updatedb",
	Short: "Update the database schema",
	Long:  `Synchronize the database schema with the models definitions.`,
	Run: func(cmd *cobra.Command, args []string) {
		projectDir := "."
		if len(args) > 0 {
			projectDir = args[0]
		}
		generateAndRunFile(projectDir, updateDBFileName, updateDBTemplate)
	},
}

// UpdateDB updates the database schema. It is meant to be called from
// a project start file which imports all the project's module.
func UpdateDB(config map[string]interface{}) {
	setupConfig(config)
	setupLogger()
	setupDebug()
	server.PreInit()
	connectToDB()
	models.BootStrap()
	models.SyncDatabase()
	resourceDir := filepath.Join(viper.GetString("ProjectDir"), "res")
	server.LoadDataRecords(resourceDir)
	if viper.GetBool("Demo") {
		log.Info("Demo mode detected: loading demo data")
		server.LoadDemoRecords(resourceDir)
	}
	log.Info("Database updated successfully")
}

func init() {
	HexyaCmd.AddCommand(updateDBCmd)
}

var updateDBTemplate = template.Must(template.New("").Parse(`
// This file is autogenerated by hexya-server
// DO NOT MODIFY THIS FILE - ANY CHANGES WILL BE OVERWRITTEN

package main

import (
	"github.com/hexya-erp/hexya/cmd"
{{ range .Imports }}	_ "{{ . }}"
{{ end }}
)

func main() {
	cmd.UpdateDB({{ .Config }})
}
`))
